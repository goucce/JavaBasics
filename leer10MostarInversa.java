package com.JavaEjercicios;

public class leer10MostarInversa {

    public static void main(String[] args) {

        int listaNumeros[] ={ 1,2,3,4,5,6,7};

        for (int i = 1; i <= listaNumeros.length; i++) {
            System.out.println(listaNumeros[listaNumeros.length-i]);
        }


    }

}
